<template>
    <inertia-link :href="href" :class="classes">
        <slot></slot>
    </inertia-link>
</template>

<script>
    export default {
        props: ['href', 'active'],

        computed: {
            classes() {
                return this.active
                            ? 'menu-link menu-toggle bg-secondary'
                            : 'menu-link'
            }
        }
    }
</script>
